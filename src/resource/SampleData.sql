drop database if exists assignment9;
create database assignment9;
use assignment9;

create table product (
	pno int auto_increment,
    pnickname varchar(30) not null,
    pname varchar(30) not null,
    pexplain longtext,
    pprice int not null,
    ppwd varchar(30) not null,
    pdate datetime default now(),
    psale varchar(10) not null default '판매중',
    constraint primary key ( pno )
);

create table inquiry(
	ino int auto_increment,
    inickname varchar(30) not null,
    iexplain longtext,
    ipwd varchar(30) not null,
    pno int not null,
    constraint primary key ( ino ),
    constraint foreign key ( pno )
		references product( pno )
);

